package com.icaikee.kline.biz.common.model;

public class WrapCenter {

	private String startTime;

	private String endTime;

	private String mode;

	private String level;

	private Double high;

	private Double low;

	private Double maxHigh;

	private Double maxLow;

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getMode() {
		return mode;
	}

	public void setMode(String mode) {
		this.mode = mode;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public Double getHigh() {
		return high;
	}

	public void setHigh(Double high) {
		this.high = high;
	}

	public Double getLow() {
		return low;
	}

	public void setLow(Double low) {
		this.low = low;
	}

	public Double getMaxHigh() {
		return maxHigh;
	}

	public void setMaxHigh(Double maxHigh) {
		this.maxHigh = maxHigh;
	}

	public Double getMaxLow() {
		return maxLow;
	}

	public void setMaxLow(Double maxLow) {
		this.maxLow = maxLow;
	}

}
